package donate7.model;

import java.util.Date;

public class Register {
	private int re_no;
	private int reporter_no;
	private int reported_no;
	private String re_content;
	private String re_chk;
	private Date re_date;
	private String re_sort;
	private int re_sort_no;
	public int getRe_no() {
		return re_no;
	}
	public void setRe_no(int re_no) {
		this.re_no = re_no;
	}
	public int getReporter_no() {
		return reporter_no;
	}
	public void setReporter_no(int reporter_no) {
		this.reporter_no = reporter_no;
	}
	public int getReported_no() {
		return reported_no;
	}
	public void setReported_no(int reported_no) {
		this.reported_no = reported_no;
	}
	public String getRe_content() {
		return re_content;
	}
	public void setRe_content(String re_content) {
		this.re_content = re_content;
	}
	public String getRe_chk() {
		return re_chk;
	}
	public void setRe_chk(String re_chk) {
		this.re_chk = re_chk;
	}
	public Date getRe_date() {
		return re_date;
	}
	public void setRe_date(Date re_date) {
		this.re_date = re_date;
	}
	public String getRe_sort() {
		return re_sort;
	}
	public void setRe_sort(String re_sort) {
		this.re_sort = re_sort;
	}
	public int getRe_sort_no() {
		return re_sort_no;
	}
	public void setRe_sort_no(int re_sort_no) {
		this.re_sort_no = re_sort_no;
	}
}
