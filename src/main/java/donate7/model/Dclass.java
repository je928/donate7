package donate7.model;

public class Dclass {
	private int dclass_no;
	private int class_no;
	private String dclass_name;
	
	public int getDclass_no() {
		return dclass_no;
	}
	public void setDclass_no(int dclass_no) {
		this.dclass_no = dclass_no;
	}
	public int getClass_no() {
		return class_no;
	}
	public void setClass_no(int class_no) {
		this.class_no = class_no;
	}
	public String getDclass_name() {
		return dclass_name;
	}
	public void setDclass_name(String dclass_name) {
		this.dclass_name = dclass_name;
	}
	
	
}
